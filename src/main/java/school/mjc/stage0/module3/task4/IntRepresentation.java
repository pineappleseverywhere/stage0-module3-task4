package school.mjc.stage0.module3.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        char char1 = 'c';
        char char2 = 'h';
        char char3 = 'a';
        char char4 = 'r';

        // Print characters using their integer representations
        int a = (int) char1;
        int b = (int) char2;
        int c = (int) char3;
        int d = (int) char4;
        char charC = (char) a;
        char charH = (char) b;
        char charA = (char) c;
        char charR = (char) d;
        System.out.print(charC);
        System.out.print( charH);
        System.out.print(charA);
        System.out.print(charR);
    }
}

